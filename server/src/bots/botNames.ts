// Использованные имена (in-memory — сбрасывается при перезапуске сервера)
const usedNames = new Set<string>();

/** Получить случайное неиспользованное имя. Возвращает null если все заняты. */
export function pickBotName(): string | null {
  const available = BOT_NAMES.filter(n => !usedNames.has(n));
  if (!available.length) return null;
  const name = available[Math.floor(Math.random() * available.length)]!;
  usedNames.add(name);
  return name;
}

/** Освободить имя (при остановке бота) */
export function releaseBotName(name: string): void {
  usedNames.delete(name);
}

/** Проверить, используется ли имя */
export function isNameUsed(name: string): boolean {
  return usedNames.has(name);
}